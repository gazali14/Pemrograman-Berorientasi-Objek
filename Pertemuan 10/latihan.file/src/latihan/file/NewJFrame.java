    private void openToggleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openToggleButtonActionPerformed
        // TODO add your handling code here:
        if(fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
            fileTextField.setText(fileChooser.getSelectedFile().toString());
            FileInputStream fis = null;
            try {
                fis = new FileInputStream(fileChooser.getSelectedFile());
                String str = "";
                while(fis.available()>0){
                    str += (char) fis.read();
                }
                contentTextArea.setText(str);
                fis.close();
            } catch (FileNotFoundException ex) {
                System.err.println("File tidak ditemukan");
            } catch (IOException ex) {
                System.err.println("File gagal dibaca");
            }
        }
    }//GEN-LAST:event_openToggleButtonActionPerformed

