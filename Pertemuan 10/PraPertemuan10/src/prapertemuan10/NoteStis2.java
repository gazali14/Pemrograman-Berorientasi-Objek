    private void openFile() {
        int returnVal = fileChooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
                String line;
                StringBuilder text = new StringBuilder();
                while ((line = reader.readLine()) != null) {
                    text.append(line).append("\n");
                }
                textArea.setText(text.toString());
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }

    private void saveFile() {
        int returnValue = fileChooser.showOpenDialog(this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            String filePath = file.getAbsolutePath();
            if (!filePath.endsWith(".txt")) {
                filePath += ".txt";
            }
            try {
                BufferedWriter writer = new BufferedWriter(new FileWriter(filePath));
                writer.write(textArea.getText());
                JOptionPane.showMessageDialog(null,"Save File Succes");
                writer.close();
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null,"Save File Failed");
                e.printStackTrace();
            }
        }
    }

    private void saveTextProcess() {
        int returnValue = fileChooser.showOpenDialog(this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            String filePath = file.getAbsolutePath();
            if (!filePath.endsWith(".txt")) {
                filePath += ".txt";
            }
            try (BufferedWriter writer = new BufferedWriter(new FileWriter(filePath))) {
                writer.write("Hasil proses text: ");
                writer.newLine();
                writer.write("Jumlah Karakter (diluar spasi): " + jumKarakterTextField.getText());
                writer.newLine();
                writer.write("Jumlah baris: " + jumBarisTextField.getText());
                writer.newLine();
                writer.write("Jumlah Huruf Vokal: " + vokalTextField.getText());
                writer.newLine();
                writer.write("Jumlah Huruf Konsonan: " + konsonanTextField.getText());
                JOptionPane.showMessageDialog(null, "Save Process File Succes");
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "Save Process File Failed");
                e.printStackTrace();
            }
        }
    }
