    private void prosesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prosesButtonActionPerformed
        // TODO add your handling code here:
        String fullText = textArea.getText().replace(" ", "");

        // Split the text into lines and initialize counters
        String[] lineText = fullText.split("\n");
        jumlahBaris = 0; charCount = 0;
        jumlahBaris = lineText.length;

        // Loop through each line and count characters
        for (String textLine : lineText) {
            charCount += textLine.length();
        }

        // Update the text fields with the number of lines and characters
        jumBarisTextField.setText(jumlahBaris.toString());
        jumKarakterTextField.setText(charCount.toString());

        // Initialize consonant and vowel counters and convert the text to lowercase
        countConsonant = 0; countVocal = 0;
        String lowercaseText = fullText.toLowerCase();

        // Loop through each character in the lowercase text
        for (int i = 0; i < lowercaseText.length(); i++) {
            char letter = lowercaseText.charAt(i);

            // If the character is a letter, check if it's a consonant or vowel
            if (letter >= 'a' && letter <= 'z') {
                if (letter != 'a' && letter != 'i' && letter != 'u' && letter != 'e' && letter != '0') {
                    countConsonant++;
                } else {
                    countVocal++;
                }
            }
        }

        // Update the text fields with the number of consonants and vowels
        konsonanTextField.setText(countConsonant.toString());
        vokalTextField.setText(countVocal.toString());
    }//GEN-LAST:event_prosesButtonActionPerformed

